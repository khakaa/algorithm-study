SELECT 
    DATE_FORMAT(Z.SALES_DATE, '%Y-%m-%d') SALES_DATE,
    Z.PRODUCT_ID,
    Z.USER_ID,
    Z.SALES_AMOUNT
FROM (
    SELECT ONLINE_SALE_ID, USER_ID, PRODUCT_ID, SALES_AMOUNT, SALES_DATE 
    FROM ONLINE_SALE
    UNION ALL
    SELECT OFFLINE_SALE_ID, NULL, PRODUCT_ID, SALES_AMOUNT, SALES_DATE 
    FROM OFFLINE_SALE
) Z
WHERE Z.SALES_DATE LIKE '2022-03%'
ORDER BY Z.SALES_DATE, Z.PRODUCT_ID, Z.USER_ID